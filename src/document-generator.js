export function createDocumentGenerator(documentPackageBuilder) {
  return {
    name: "Генератор документов",
    generateDocuments: () => generateDocuments(documentPackageBuilder),
    runDocumentChecks: () => runDocumentChecks(generateDocuments(documentPackageBuilder))
  };
}

export function generateDocuments(documentPackageBuilder) {
  const packages = documentPackageBuilder.buildPackages();
  return packages.map((pkg) => ({
    chapterId: pkg.chapterId,
    chapterTitle: pkg.chapterTitle,
    documents: selectDocumentsForGeneration(pkg).map((spec) => generateDocument(spec, pkg.chapterId))
  }));
}

function selectDocumentsForGeneration(pkg) {
  if (pkg.chapterId === "chapter-2") {
    return [
      ...pkg.documents.slice(0, 2),
      pkg.documents.find((spec) => spec.title.includes("Заметки Артура"))
    ].filter(Boolean);
  }
  return pkg.documents.slice(0, 2);
}

export function runDocumentChecks(generatedPackages) {
  return generatedPackages.flatMap((pkg) =>
    pkg.documents.map((doc) => {
      const hasOrdinaryInfo = doc.ordinaryDetails.length > 0;
      const doesNotExplainPlot = !doc.body.toLowerCase().includes("убийца") && !doc.body.toLowerCase().includes("игрок");
      const hasHiddenFact = doc.hiddenFacts.length > 0;
      return {
        chapter: pkg.chapterTitle,
        document: doc.title,
        looksReal: doc.body.length > 300,
        matchesAuthor: Boolean(doc.author && doc.type),
        hasOrdinaryInfo,
        notAHint: doesNotExplainPlot,
        importantFactInferable: hasHiddenFact,
        hasLaterReinterpretation: doc.reinterpretableDetails.length > 0,
        status:
          doc.body.length > 300 &&
          Boolean(doc.author && doc.type) &&
          hasOrdinaryInfo &&
          doesNotExplainPlot &&
          hasHiddenFact &&
          doc.reinterpretableDetails.length > 0
            ? "готов"
            : "требует переработки"
      };
    })
  );
}

function generateDocument(spec, chapterId) {
  const generated = generatorFor(spec.title, chapterId)(spec);
  return {
    type: spec.type,
    title: spec.title,
    author: spec.author,
    recipient: spec.recipient,
    date: spec.date,
    body: generated.body,
    hiddenFacts: spec.hiddenFacts.map((fact) => ({ fact, visibility: visibilityFor(spec, fact) })),
    gameFunction: spec.gameFunction,
    ordinaryDetails: generated.ordinaryDetails,
    reinterpretableDetails: generated.reinterpretableDetails
  };
}

function generatorFor(title) {
  if (title.includes("Реестр материалов, переданных стороне защиты")) return generateDefenseMaterialsRegister;
  if (title.includes("допроса Сергея")) return generateSergeyInterrogation;
  if (title.includes("Письмо с вложением")) return generateBlondeEmail;
  if (title.includes("Рецепт на очки")) return generatePrescription;
  if (title.includes("CRM")) return generateCrmExport;
  if (title.includes("Газетный номер")) return generateMiyaNewspaperIssue;
  if (title.includes("Заметки Артура")) return generateArturMiyaNotes;
  if (title.includes("взломанной калитки")) return generateBrokenGateReport;
  if (title.includes("ножа с террасы")) return generateTerraceKnifeProtocol;
  if (title.includes("камеры")) return generateCameraProtocol;
  return generateGenericDocument;
}

