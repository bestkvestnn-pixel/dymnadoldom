function checkChapter(chapterId, architecture) {
  const checks = [
    ["Есть ли в главе новое открытие?", Boolean(architecture.finale.learned)],
    ["Есть ли в главе новый вопрос?", architecture.nextChapter.questions.length > 0],
    ["Есть ли хотя бы один ошибочный путь расследования?", architecture.materials.some((item) => item.function.includes("усилить версию"))],
    ["Есть ли хотя бы одна важная улика?", architecture.clueLayers.length > 0],
    ["Может ли игрок сделать логический вывод самостоятельно?", architecture.deductionChain.length >= 3],
    ["Не раскрыта ли разгадка слишком рано?", chapterId === "chapter-3" || !architecture.finale.learned.includes("Виктор убил Анну")]
  ];

  return checks.map(([question, passed]) => ({
    question,
    answer: passed ? "да" : "нет",
    passed
  }));
}

function material(name, fn, visibleFact, hiddenFunction) {
  return {
    name,
    function: fn,
    visibleFact,
    hiddenFunction
  };
}

function chapterIdFromTitle(title) {
  if (title.includes("Семейная")) return "chapter-1";
  if (title.includes("Серия")) return "chapter-2";
  if (title.includes("Финальная")) return "chapter-3";
  return "";
}
