import { buildChapterPlan, buildEvidenceFunctions } from "./scenario-writer.js";

export function createChapterArchitect(canon) {
  return {
    name: "Архитектор главы",
    buildArchitectures: () => buildChapterArchitectures(canon),
    inspectChapter: (chapterId) => buildChapterArchitecture(canon, chapterId)
  };
}

export function buildChapterArchitectures(canon) {
  return canon.chapters.map((chapter) => buildChapterArchitecture(canon, chapter.id));
}

export function buildChapterArchitecture(canon, chapterId) {
  const plan = buildChapterPlan(canon).find((chapter) => chapterIdFromTitle(chapter.title) === chapterId || chapter.id === chapterId);
  const sourceChapter = canon.chapters.find((chapter) => chapter.id === chapterId);
  const resolvedPlan = plan || buildChapterPlan(canon).find((chapter) => chapter.title === sourceChapter?.title);
  const chapter = sourceChapter || canon.chapters[0];
  const architecture = architectureFor(chapter.id, resolvedPlan || chapter);
  const materialPackage = architecture.materials.map((material) => ({
    ...material,
    rule: material.function ? "меняет понимание дела" : "слабый материал"
  }));

  return {
    id: chapter.id,
    title: chapter.title,
    mainQuestion: architecture.mainQuestion,
    tempo: architecture.tempo,
    beforeAfter: architecture.beforeAfter,
    suspicion: architecture.suspicion,
    materials: materialPackage,
    informationBalance: architecture.informationBalance,
    deductionChain: architecture.deductionChain,
    clueLayers: architecture.clueLayers,
    finale: architecture.finale,
    nextChapter: architecture.nextChapter,
      check: checkChapter(chapter.id, architecture)
  };
}

