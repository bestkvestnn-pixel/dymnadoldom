function checkPackage(documents, hiddenFacts, crossLinks) {
  const hasFunctionless = documents.some((doc) => !doc.gameFunction);
  const singleSourceFacts = hiddenFacts.filter((item) => item.sources.length < 2);
  const tooObviousKey = hiddenFacts.some((item) => item.visibility === "очевидный" && item.fact.includes("Виктор"));
  return {
    noFunctionlessDocuments: !hasFunctionless,
    noSingleSourceKeyFacts: singleSourceFacts.length === 0,
    keyCluesNotTooObvious: !tooObviousKey,
    canLinkDocuments: crossLinks.length > 0,
    documentsLookNatural: documents.every((doc) => doc.worldPurpose && doc.reason && !doc.visibleText.includes("убийца")),
    createsNewQuestions: documents.some((doc) => doc.newQuestions.length > 0),
    status:
      !hasFunctionless && singleSourceFacts.length === 0 && !tooObviousKey && crossLinks.length > 0
        ? "готов"
        : "требует переработки"
  };
}

