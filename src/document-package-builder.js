export function createDocumentPackageBuilder(canon, clueConstructor) {
  return {
    name: "Конструктор пакета документов",
    buildPackages: () => buildDocumentPackages(canon, clueConstructor),
    buildPlacementOptions: () => buildPlacementOptions(),
    runPackageChecks: () => runPackageChecks(buildDocumentPackages(canon, clueConstructor))
  };
}

export function buildDocumentPackages(canon, clueConstructor) {
  const clueSystem = clueConstructor.buildClueSystem();
  return clueSystem.map((chapter) => {
    const documents = documentsForChapter(chapter.chapterId);
    return {
      chapterId: chapter.chapterId,
      chapterTitle: chapter.chapterTitle,
      goal: goalForChapter(chapter.chapterId),
      documents,
      hiddenFacts: hiddenFactsForChapter(chapter.chapterId),
      falseConclusions: falseConclusionsForChapter(chapter.chapterId),
      trueConclusions: trueConclusionsForChapter(chapter.chapterId),
      crossLinks: crossLinksForChapter(chapter.chapterId),
      check: checkPackage(documents, hiddenFactsForChapter(chapter.chapterId), crossLinksForChapter(chapter.chapterId))
    };
  });
}

