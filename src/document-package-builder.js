export function runPackageChecks(packages) {
  return packages.map((pkg) => {
    const functionless = pkg.documents.filter((doc) => !doc.gameFunction);
    const singleSourceFacts = pkg.hiddenFacts.filter((fact) => fact.sources.length < 2);
    const tooObvious = pkg.hiddenFacts.filter((fact) => fact.visibility === "очевидный" && fact.fact.includes("Виктор"));
    const hasCrossLinks = pkg.crossLinks.length > 0;
    const hasQuestions = pkg.documents.some((doc) => doc.newQuestions.length > 0);

    return {
      chapter: pkg.chapterTitle,
      noFunctionlessDocuments: functionless.length === 0,
      noSingleSourceKeyFacts: singleSourceFacts.length === 0,
      keyCluesNotTooObvious: tooObvious.length === 0,
      documentsCanBeLinked: hasCrossLinks,
      documentsLookNatural: pkg.documents.every((doc) => doc.worldPurpose && doc.author && doc.reason),
      createsNewQuestions: hasQuestions,
      status:
        functionless.length === 0 &&
        singleSourceFacts.length === 0 &&
        tooObvious.length === 0 &&
        hasCrossLinks &&
        hasQuestions
          ? "готов"
          : "требует доработки"
    };
  });
}

