export function detectMode(input = "") {
  const text = input.toLowerCase();
  const hasSourceData =
    containsAny(text, drawingKeywords) ||
    containsAny(text, partDescriptionKeywords) ||
    /\d+\s*[xх×*]\s*\d+/.test(text) ||
    /[øØ]\s*\d+/.test(input);

  if (!hasSourceData) {
    return "data-request";
  }

  const matched = modeKeywords.find(([, words]) => containsAny(text, words));
  return matched ? matched[0] : "full-process";
}

